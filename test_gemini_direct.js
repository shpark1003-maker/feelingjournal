const { callGemini } = require('./api/_routes/clients/gemini');

async function testGemini() {
    const prompt = `
너는 사용자의 하루를 책임지는 완벽하고 꼼꼼한 감성 수석 비서다. 아래 데이터를 참고하여 품격 있고 깊이감 있는 오늘의 데일리 브리핑을 작성하라.
사용자의 호칭은 "선호 님"이다. 브리핑 시작과 끝에 반드시 이 호칭으로 직접 불러 정성껏 안내하십시오.

[실시간 수집 데이터]
1. 현재 시간: 2026-07-08T02:31:18+09:00
2. 구글 일정 (어제~내일): 일정 정보 없음
3. 최근 생각(Diary) (작성일 포함): 일기 기록 없음
4. 실시간 기상 예보: 날씨 안내 비활성화됨
5. 연계된 과거의 기억(Reminiscence): 특별한 과거 회상 없음
6. 밀린 과제 점검: [오늘 가장 시급한 과제 후보]: "관심 분야의 최근 3년 내 주요 학술지 논문 5편을 선정하여 읽고 요약하기" (마감: 2026-06-25, 달성률: 40%) - 이를 브리핑 최우선 과제로 고려하십시오.

[수행 지시]
1. 브리핑 구조 및 우선순위 엄수: 브리핑은 산만하지 않게 다음의 5가지 흐름으로 자연스럽게 서술하십시오.
   ① 오늘의 핵심 (가장 시급한 과제나 주요 일정 짚어주기)
   ② 오늘의 일정 (구글 일정 기반)
   ③ 밀린 과제 점검 (존재할 경우 단호한 동기부여)
   ④ 실시간 날씨 및 외출 조언
   ⑤ 오늘의 한마디 (과거 회상과 엮어 따뜻한 격려)
2. 분량: 전체 브리핑은 8~12문장 정도로 상세하게 작성하십시오. 중요한 일정이나 키워드는 **텍스트** 로 강조하십시오.
3. 응답 잘림 방지: 응답 텍스트가 도중에 끊어지는 일이 없도록 분량을 조절하여 반드시 **완전한 문장**으로 서술을 마무리지으십시오.
4. 오늘 가장 먼저 해야 할 일 (마무리 포맷): 브리핑의 맨 마지막에는 어떠한 인사말도 덧붙이지 말고, 의미적으로 다음 형태를 유지하여 브리핑을 끝마치십시오. (기호나 서식은 유연하게 하되, 제목과 1가지 핵심 행동이 명확히 보이도록 할 것)

제목: 오늘 가장 먼저 해야 할 일
내용: [가장 중요한 과제나 일정 1가지]
`;

    try {
        console.log("Calling Gemini...");
        const data = await callGemini(prompt, { maxOutputTokens: 4096 }, 3, null, true);
        const text = data?.candidates?.[0]?.content?.parts?.[0]?.text;
        console.log("Generated Text:", text);
        console.log("Length:", text.length);
        console.log("Finish Reason:", data?.candidates?.[0]?.finishReason);
    } catch (e) {
        console.error("Error:", e);
    }
}

testGemini();
